-- ── set_updated_at ───────────────────────────────────────────
-- Generic BEFORE UPDATE trigger to stamp updated_at = now()
CREATE OR REPLACE FUNCTION public.set_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$;

COMMENT ON FUNCTION public.set_updated_at() IS
  'Generic updated_at stamper. Attach as BEFORE UPDATE trigger to any table with updated_at.';

-- ── fn_auto_create_employee ──────────────────────────────────
-- Fires AFTER INSERT on public.users.
-- Automatically creates a linked employees row so every user has an employee profile.
-- Admins get ADM#### codes, employees get EMP#### codes.
CREATE OR REPLACE FUNCTION public.fn_auto_create_employee()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_code    TEXT;
  v_prefix  TEXT;
  v_seq_num BIGINT;
BEGIN
  v_prefix  := CASE WHEN NEW.role = 'ADMIN' THEN 'ADM' ELSE 'EMP' END;
  v_seq_num := nextval('public.employee_code_seq');
  v_code    := v_prefix || LPAD(v_seq_num::text, 4, '0');

  INSERT INTO public.employees (
    id, organization_id, user_id, name, phone,
    employee_code, is_active, last_activity_at, created_at, updated_at
  ) VALUES (
    gen_random_uuid(),
    NEW.organization_id,
    NEW.id,
    initcap(replace(split_part(NEW.email, '@', 1), '.', ' ')),
    NULL,
    v_code,
    true,
    NULL,
    now(),
    now()
  );

  RETURN NEW;
END;
$$;

COMMENT ON FUNCTION public.fn_auto_create_employee() IS
  'Auto-creates employees row on users INSERT.
   Role-aware prefix: ADMIN → ADM####, EMPLOYEE → EMP####.
   Uses employee_code_seq to prevent race conditions on concurrent signups.';

-- ── fn_update_employee_latest_session ────────────────────────
-- Fires AFTER INSERT OR UPDATE on attendance_sessions.
-- Upserts the employee_latest_sessions snapshot row.
-- Includes staleness guard: will not overwrite a newer session.
-- This is critical — BullMQ distance jobs update old sessions;
-- without the guard those updates would corrupt the snapshot.
CREATE OR REPLACE FUNCTION public.fn_update_employee_latest_session()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  -- Staleness guard: skip if a newer session is already in the snapshot
  IF EXISTS (
    SELECT 1 FROM public.employee_latest_sessions els
    WHERE els.employee_id   = NEW.employee_id
      AND els.latest_checkin > NEW.checkin_at
      AND els.session_id    <> NEW.id
  ) THEN
    RETURN NEW;
  END IF;

  INSERT INTO public.employee_latest_sessions (
    employee_id, organization_id, session_id,
    latest_checkin, latest_checkout,
    total_distance_km, total_duration_seconds,
    status, updated_at
  )
  VALUES (
    NEW.employee_id,
    NEW.organization_id,
    NEW.id,
    NEW.checkin_at,
    NEW.checkout_at,
    NEW.total_distance_km,
    NEW.total_duration_seconds,
    CASE
      WHEN NEW.checkout_at IS NULL                        THEN 'ACTIVE'
      WHEN NEW.checkout_at > now() - interval '24 hours' THEN 'RECENT'
      ELSE                                                     'INACTIVE'
    END,
    now()
  )
  ON CONFLICT (employee_id) DO UPDATE SET
    session_id             = EXCLUDED.session_id,
    organization_id        = EXCLUDED.organization_id,
    latest_checkin         = EXCLUDED.latest_checkin,
    latest_checkout        = EXCLUDED.latest_checkout,
    total_distance_km      = EXCLUDED.total_distance_km,
    total_duration_seconds = EXCLUDED.total_duration_seconds,
    status                 = EXCLUDED.status,
    updated_at             = now();

  RETURN NEW;
END;
$$;

COMMENT ON FUNCTION public.fn_update_employee_latest_session() IS
  'Maintains employee_latest_sessions snapshot on every attendance_sessions change.
   Staleness guard prevents BullMQ distance recalculation jobs on old sessions
   from overwriting a more recent session in the snapshot.';

-- ── fn_sync_summary_to_latest_session ────────────────────────
-- Fires AFTER INSERT OR UPDATE on session_summaries.
-- Patches computed distance and duration into the snapshot row,
-- but only if the summary belongs to the employee''s current latest session.
CREATE OR REPLACE FUNCTION public.fn_sync_summary_to_latest_session()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  UPDATE public.employee_latest_sessions els
  SET
    total_distance_km      = NEW.total_distance_km,
    total_duration_seconds = NEW.total_duration_seconds,
    updated_at             = now()
  FROM public.attendance_sessions s
  WHERE s.id            = NEW.session_id
    AND els.employee_id = s.employee_id
    AND els.session_id  = NEW.session_id;

  RETURN NEW;
END;
$$;

COMMENT ON FUNCTION public.fn_sync_summary_to_latest_session() IS
  'Patches distance/duration into employee_latest_sessions when session_summaries changes.
   Only applies if the summary belongs to the employee''s current latest session.';

-- ── get_org_latest_sessions ──────────────────────────────────
-- Paginated, filterable sessions query for the admin dashboard.
-- NOTE: This queries attendance_sessions directly for historical accuracy.
-- For the live dashboard snapshot, read employee_latest_sessions directly.
CREATE OR REPLACE FUNCTION public.get_org_latest_sessions(
  p_org_id UUID,
  p_status TEXT    DEFAULT 'all',
  p_limit  INTEGER DEFAULT 20,
  p_offset INTEGER DEFAULT 0
)
RETURNS TABLE (
  id                           UUID,
  employee_id                  UUID,
  organization_id              UUID,
  checkin_at                   TIMESTAMPTZ,
  checkout_at                  TIMESTAMPTZ,
  total_distance_km            FLOAT8,
  total_duration_seconds       INT,
  distance_recalculation_status TEXT,
  created_at                   TIMESTAMPTZ,
  updated_at                   TIMESTAMPTZ,
  employee_code                TEXT,
  employee_name                TEXT,
  activity_status              TEXT,
  total_count                  BIGINT
)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  RETURN QUERY
  WITH latest AS (
    SELECT DISTINCT ON (s.employee_id)
      s.id, s.employee_id, s.organization_id,
      s.checkin_at, s.checkout_at,
      s.total_distance_km, s.total_duration_seconds,
      s.distance_recalculation_status::TEXT,
      s.created_at, s.updated_at,
      e.employee_code,
      e.name AS employee_name,
      CASE
        WHEN s.checkout_at IS NULL                        THEN 'ACTIVE'
        WHEN s.checkout_at > NOW() - INTERVAL '24 hours' THEN 'RECENT'
        ELSE                                                   'INACTIVE'
      END AS activity_status
    FROM attendance_sessions s
    JOIN employees e ON e.id = s.employee_id
    WHERE s.organization_id = p_org_id
    ORDER BY s.employee_id, s.checkin_at DESC
  ),
  filtered AS (
    SELECT * FROM latest
    WHERE
      p_status = 'all'
      OR (p_status = 'active'   AND activity_status = 'ACTIVE')
      OR (p_status = 'recent'   AND activity_status = 'RECENT')
      OR (p_status = 'inactive' AND activity_status = 'INACTIVE')
  ),
  counted AS (
    SELECT *, COUNT(*) OVER() AS total_count
    FROM filtered
    ORDER BY
      CASE activity_status
        WHEN 'ACTIVE'  THEN 1
        WHEN 'RECENT'  THEN 2
        ELSE                3
      END,
      checkin_at DESC
  )
  SELECT * FROM counted
  LIMIT p_limit OFFSET p_offset;
END;
$$;

COMMENT ON FUNCTION public.get_org_latest_sessions(UUID, TEXT, INTEGER, INTEGER) IS
  'Paginated admin sessions dashboard query. Returns latest session per employee
   with activity_status and total_count for pagination.
   p_status: ''all'' | ''active'' | ''recent'' | ''inactive''';

