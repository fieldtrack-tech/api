-- SEQUENCES
-- ══════════════════════════════════════════════════════════════

-- Collision-safe employee code generator (EMP#### / ADM####)
-- Starts at 1. Shared across both prefixes — sequence value is the number,
-- prefix is determined at insert time by role.
CREATE SEQUENCE IF NOT EXISTS public.employee_code_seq
  START WITH 1
  INCREMENT BY 1
  NO CYCLE;

