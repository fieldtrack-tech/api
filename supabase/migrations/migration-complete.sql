CREATE TYPE public.user_role AS ENUM ('ADMIN', 'EMPLOYEE');

CREATE TYPE public.expense_status AS ENUM ('PENDING', 'APPROVED', 'REJECTED');

CREATE TYPE public.distance_job_status AS ENUM (
  'pending',
  'processing',
  'done',
  'failed'
);

