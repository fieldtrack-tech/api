-- ── employee_latest_sessions ─────────────────────────────────
CREATE TABLE IF NOT EXISTS public.employee_latest_sessions (
  employee_id            UUID        PRIMARY KEY REFERENCES public.employees(id) ON DELETE CASCADE,
  organization_id        UUID        NOT NULL REFERENCES public.organizations(id) ON DELETE CASCADE,
  session_id             UUID        REFERENCES public.attendance_sessions(id) ON DELETE SET NULL,
  latest_checkin         TIMESTAMPTZ,
  latest_checkout        TIMESTAMPTZ,
  total_distance_km      FLOAT8,
  total_duration_seconds INT,
  status                 TEXT        CHECK (status IN ('ACTIVE', 'RECENT', 'INACTIVE')),
  updated_at             TIMESTAMPTZ DEFAULT now()
);

COMMENT ON TABLE public.employee_latest_sessions IS
  'Snapshot of each employee''s latest session state. Source of truth for dashboard reads.
   One row per employee, maintained automatically by triggers.
   NEVER scan attendance_sessions for dashboard queries — read from here instead.
   Status lifecycle: ACTIVE (trigger) → RECENT (trigger) → INACTIVE (pg_cron hourly).';

