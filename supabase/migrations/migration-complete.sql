-- PG_CRON — STATUS REFRESH JOB
-- ══════════════════════════════════════════════════════════════
-- Flips RECENT → INACTIVE after 24 hours of inactivity.
-- Runs every hour at :00.
-- ACTIVE → RECENT transition is handled instantly by the trigger.
-- RECENT → INACTIVE cannot be done by a trigger (time-based) so uses cron.

DO $outer$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM cron.job WHERE jobname = 'employee-status-refresh'
  ) THEN
    PERFORM cron.schedule(
      'employee-status-refresh',
      '0 * * * *',
      $cron$
        UPDATE public.employee_latest_sessions
        SET
          status     = 'INACTIVE',
          updated_at = now()
        WHERE status          = 'RECENT'
          AND latest_checkout < now() - interval '24 hours';
      $cron$
    );
  END IF;
END;
$outer$;

