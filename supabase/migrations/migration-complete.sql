-- ── organizations ────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.organizations (
  id         UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  name       TEXT        NOT NULL,
  slug       TEXT        UNIQUE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

COMMENT ON TABLE public.organizations IS
  'Top-level tenant. All other tables scope to organization_id.';

-- ── users ────────────────────────────────────────────────────
-- Mirrors auth.users but scoped to an organization with a role.
-- This is the application user table (not auth.users directly).
CREATE TABLE IF NOT EXISTS public.users (
  id              UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  organization_id UUID        NOT NULL REFERENCES public.organizations(id) ON DELETE CASCADE,
  email           TEXT        NOT NULL UNIQUE,
  role            public.user_role NOT NULL,
  created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);

COMMENT ON TABLE public.users IS
  'Application user profiles linked to auth.users via shared UUID and email.';

-- ── employees ────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.employees (
  id               UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  organization_id  UUID        NOT NULL REFERENCES public.organizations(id) ON DELETE CASCADE,
  user_id          UUID        UNIQUE REFERENCES public.users(id) ON DELETE SET NULL,
  name             TEXT        NOT NULL,
  phone            TEXT,
  is_active        BOOLEAN     NOT NULL DEFAULT true,
  employee_code    TEXT        NOT NULL UNIQUE,
  last_activity_at TIMESTAMPTZ,
  created_at       TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at       TIMESTAMPTZ NOT NULL DEFAULT now()
);

COMMENT ON COLUMN public.employees.employee_code IS
  'Human-readable identifier e.g. EMP0001 or ADM0001. Generated by sequence. UUID id is the PK.';
COMMENT ON COLUMN public.employees.last_activity_at IS
  'Updated on check-in / check-out. Used to compute ACTIVE / RECENT / INACTIVE status.';

-- ── attendance_sessions ──────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.attendance_sessions (
  id                             UUID                       PRIMARY KEY DEFAULT gen_random_uuid(),
  organization_id                UUID                       NOT NULL REFERENCES public.organizations(id) ON DELETE CASCADE,
  employee_id                    UUID                       NOT NULL REFERENCES public.employees(id) ON DELETE CASCADE,
  checkin_at                     TIMESTAMPTZ                NOT NULL DEFAULT now(),
  checkout_at                    TIMESTAMPTZ,
  distance_recalculation_status  public.distance_job_status NOT NULL DEFAULT 'pending',
  total_distance_km              FLOAT8,
  total_duration_seconds         INT,
  created_at                     TIMESTAMPTZ                NOT NULL DEFAULT now(),
  updated_at                     TIMESTAMPTZ                NOT NULL DEFAULT now()
);

COMMENT ON TABLE public.attendance_sessions IS
  'One row per field session (check-in to check-out). Source for GPS trails and distance jobs.';

-- ── gps_locations ────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.gps_locations (
  id              UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  organization_id UUID        NOT NULL REFERENCES public.organizations(id) ON DELETE CASCADE,
  session_id      UUID        NOT NULL REFERENCES public.attendance_sessions(id) ON DELETE CASCADE,
  employee_id     UUID        NOT NULL REFERENCES public.employees(id) ON DELETE CASCADE,
  latitude        FLOAT8      NOT NULL CHECK (latitude  >= -90  AND latitude  <= 90),
  longitude       FLOAT8      NOT NULL CHECK (longitude >= -180 AND longitude <= 180),
  accuracy        FLOAT8,
  recorded_at     TIMESTAMPTZ NOT NULL,
  sequence_number INT,
  is_duplicate    BOOLEAN     NOT NULL DEFAULT false,
  UNIQUE (session_id, recorded_at)
);

COMMENT ON TABLE public.gps_locations IS
  'Raw GPS breadcrumb trail per session. Duplicate flag set by distance calculation job.';

-- ── expenses ─────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.expenses (
  id              UUID                   PRIMARY KEY DEFAULT gen_random_uuid(),
  organization_id UUID                   NOT NULL REFERENCES public.organizations(id) ON DELETE CASCADE,
  employee_id     UUID                   NOT NULL REFERENCES public.employees(id) ON DELETE CASCADE,
  amount          NUMERIC                NOT NULL CHECK (amount > 0),
  description     TEXT                   NOT NULL,
  status          public.expense_status  NOT NULL DEFAULT 'PENDING',
  receipt_url     TEXT,
  submitted_at    TIMESTAMPTZ            NOT NULL DEFAULT now(),
  reviewed_at     TIMESTAMPTZ,
  reviewed_by     UUID                   REFERENCES public.users(id) ON DELETE SET NULL,
  created_at      TIMESTAMPTZ            NOT NULL DEFAULT now(),
  updated_at      TIMESTAMPTZ            NOT NULL DEFAULT now()
);

-- ── session_summaries ────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.session_summaries (
  id                     UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  organization_id        UUID        NOT NULL REFERENCES public.organizations(id) ON DELETE CASCADE,
  session_id             UUID        NOT NULL UNIQUE REFERENCES public.attendance_sessions(id) ON DELETE CASCADE,
  total_distance_km      FLOAT8      NOT NULL DEFAULT 0,
  total_duration_seconds INT         NOT NULL DEFAULT 0,
  avg_speed_kmh          FLOAT8      NOT NULL DEFAULT 0,
  computed_at            TIMESTAMPTZ NOT NULL DEFAULT now(),
  created_at             TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at             TIMESTAMPTZ NOT NULL DEFAULT now()
);

COMMENT ON TABLE public.session_summaries IS
  'Computed distance/duration/speed per session. Written by BullMQ distance recalculation job.';

-- ── admin_sessions ───────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.admin_sessions (
  id              UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  admin_id        UUID        NOT NULL REFERENCES public.users(id) ON DELETE CASCADE,
  organization_id UUID        NOT NULL REFERENCES public.organizations(id) ON DELETE CASCADE,
  started_at      TIMESTAMPTZ NOT NULL,
  ended_at        TIMESTAMPTZ,
  created_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);

COMMENT ON TABLE public.admin_sessions IS
